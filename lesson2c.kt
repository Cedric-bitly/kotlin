fun main() {
//  If...else...if conditional statement => used to evaliate multiple conditions
// The else part gets executed when all the conditions given evaluate to false

    val number  = -21

    if (number > 0) {
        println("The number is positive")
    } else if (number == 0) {
        println("The number is zero")
    } 
    else{
        println("The number is negative")
    }
}