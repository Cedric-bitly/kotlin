fun main(){
    // A variable is a container that stores data/values
    // In kotlin we have two types of initializers i.e. var and val
    // var is a mutable variable which means we can change its value
    // val is an immutable variable which means we cannot change its value once it is assigned

    val name = "Cedric" //string variable - it a series of characters inside double quotes
    val number1 = 10 //integer variable - it is a whole number
    val number2 = 3.14 //double variable - it is a number with a decimal point
    var letter = 'A' //char variable - it is a single letter of the alphabet
    var isRegistered = true //boolean variable - it evaluates to either true or false


    println("My name is $name")
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)
}